        if (jcmbCurrency.getSelectedItem().equals("UK")) {
            String cConvert1 = String.format("¬£ %.2f", Bangladeshi_Tk * UK_Pound);
            jlblConvert.setText(cConvert1);
        }
        if (jcmbCurrency.getSelectedItem().equals("USA")) {
            String cConvert2 = String.format("$ %.2f", Bangladeshi_Tk * US_Dollar);
            jlblConvert.setText(cConvert2);
        }
