    private void jbtnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnTotalActionPerformed
        SubClass_Child ItemCost = new SubClass_Child();
        double iTax, iSubTotal, iTotal;

        ItemCost.ChickenBurger = ItemCost.pChickenBurger * Double.parseDouble(jtxtChickBurger.getText());
        ItemCost.ChickenLegend = ItemCost.pChickenLegend * Double.parseDouble(jtxtChickLegend.getText());
        ItemCost.CheeseBurger = ItemCost.pCheeseBurger * Double.parseDouble(jtxtCheesBurger.getText());
        ItemCost.ChickenBurgerM = ItemCost.pChickenBurgerM * Double.parseDouble(jtxtChickBurgerM.getText());

        ItemCost.MilkShake = ItemCost.pMilkShake * Double.parseDouble(jtxtMilkShake.getText());
        ItemCost.VinilaCone = ItemCost.pVinilaCone * Double.parseDouble(jtxtVinilaCone.getText());
        ItemCost.VinilaMilkShake = ItemCost.pVinilaMilkShake * Double.parseDouble(jtxtVinilaMilkShake.getText());
        ItemCost.ChocolateMilkShake = ItemCost.pChocolateMilkShake * Double.parseDouble(jtxtChocolateMilkShake.getText());

        iSubTotal = ItemCost.GetAmount();
        iTax = ItemCost.cFindTax(iSubTotal);
        iTotal = iSubTotal + iTax;

        String SubTotal = String.format("Tk. %.2f", iSubTotal);
        jtxtSubTotal.setText(SubTotal);

        String Tax = String.format("Tk. %.2f", iTax);
        jtxtTax.setText(Tax);

        String Total = String.format("Tk. %.2f", iTotal);
        jtxtTotal.setText(Total);

        String Meal = String.format("Tk. %.2f", ItemCost.Meals);
        jtxtCostOfMeal.setText(Meal);

        String Drink = String.format("Tk. %.2f", ItemCost.Drinks);
        jtxtCostOfDrink.setText(Drink);

        String TotalCost = String.format("Tk. %.2f", ItemCost.TotalofMD);
        jtxtTotalCostOfItems.setText(TotalCost);


    }//GEN-LAST:event_jbtnTotalActionPerformed

